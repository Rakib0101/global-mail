<script>
    document.addEventListener('DOMContentLoaded', function() {
        const featureItems = document.querySelectorAll('.feature-item');
        const featureContents = document.querySelectorAll('.feature-content');

        featureItems.forEach(item => {
            item.addEventListener('click', function() {
                const targetFeature = this.getAttribute('data-feature');

                // Remove active styling from all feature items
                featureItems.forEach(feature => {
                    feature.classList.remove('bg-blue-50', 'border-l-4',
                        'border-blue-600');
                });

                // Add active styling to clicked feature item
                this.classList.add('bg-blue-50', 'border-l-4', 'border-blue-600');

                // Hide all feature contents
                featureContents.forEach(content => {
                    content.classList.add('hidden');
                });

                // Show target feature content
                const targetContent = document.getElementById(targetFeature);
                if (targetContent) {
                    targetContent.classList.remove('hidden');
                }
            });

            // Add hover effects
            item.addEventListener('mouseenter', function() {
                if (!this.classList.contains('bg-blue-50')) {
                    this.classList.add('bg-gray-50');
                }
            });

            item.addEventListener('mouseleave', function() {
                if (!this.classList.contains('bg-blue-50')) {
                    this.classList.remove('bg-gray-50');
                }
            });
        });
    });
</script>

